    /**
     * Mouse clicked event to post a link.
     *
     * @author Adri
     * @version 0.1.0
     * @since 05-27-2017
     * @param evt
     */
    private void botonLinkMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonLinkMouseClicked
        try {
            URL url = new URL(link.getText());
            postLink(url, leyendaLink.getText());
            leyendaLink.setText(null);
            link.setText(null);
        } catch (MalformedURLException ex) {
            Logger.getLogger(Window.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_botonLinkMouseClicked

