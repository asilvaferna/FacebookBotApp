    /**
     * This method post a message on Facebook
     *
     * @author Adri
     * @version 0.1.0
     * @since 05-27-2017
     * @param mensaje
     */
    public void postMessage(String mensaje) {
        try {
            facebook.postStatusMessage(mensaje);
        } catch (FacebookException ex) {
            Logger.getLogger(Window.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    /**
     * This method post a link with a title.
     *
     * @author Adri
     * @version 0.1.0
     * @since 05-27-2017
     * @param url
     * @param leyenda
     */
    public void postLink(URL url, String leyenda) {
        try {
            facebook.postLink(url, leyenda);
        } catch (FacebookException ex) {
            Logger.getLogger(Window.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

